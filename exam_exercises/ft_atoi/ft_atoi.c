#include <unistd.h>

int	ft_atoi(const char *str)
{
	int	i;
	int	s;
	int	res;

	i = 0;
	s = 1;
	res = 0;
	while ((str[i] == 32) || (9 <= str[i] && str[i] <= 13))
		++i;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			s = -s;
		++i;
	}
	while ('0' <= str[i] && str[i] <= '9' && str[i] != '\0')
	{
		res = res * 10 + (str[i] - '0');
		++i;
	}
	return (res * s);
}
